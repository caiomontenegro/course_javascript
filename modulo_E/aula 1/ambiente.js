// Estrutura While - Estrutura de repetição com teste lógico no início

let c = 1

while(c <= 6) {
    console.log(`Olá mundo ${c}`)
    c++
}

// Estrutura Do while - Estrutura de repetção com steste lógico no final

let x = 1

do {
    console.log(`Olá cidade ${x}`)
    x++
} while (x > 6)





